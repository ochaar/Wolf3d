#include "libft.h"

void	ft_print_tab(int **tab)
{
	int i;
	int j;

	i = 0;
	while (i < 10)
	{
		j = 0;
		while (j < 10)
		{
			ft_putnbr(tab[i][j]);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}